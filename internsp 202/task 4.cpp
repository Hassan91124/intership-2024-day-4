#include <iostream>
#include <string>

using namespace std;

int main() {
    int myInt = 10;
    float myFloat = 3.14f;
    string myString = "Hello";
    char myChar = 'A';
    double myDouble = 3.14159;
    bool myBool = true;

    cout << "Integer: " << myInt << endl;
    cout << "Float: " << myFloat << endl;
    cout << "String: " << myString << endl;
    cout << "Char: " << myChar << endl;
    cout << "Double: " << myDouble << endl;
    cout << "Bool: " << myBool << endl;

    return 0;
}
